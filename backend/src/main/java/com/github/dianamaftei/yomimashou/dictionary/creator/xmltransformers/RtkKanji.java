package com.github.dianamaftei.yomimashou.dictionary.creator.xmltransformers;

public class RtkKanji {

  private String kanji;
  private String components;
  private String keyword;
  private String story1;
  private String story2;

  public String getKanji() {
    return kanji;
  }

  public void setKanji(String kanji) {
    this.kanji = kanji;
  }

  public String getComponents() {
    return components;
  }

  public void setComponents(String components) {
    this.components = components;
  }

  public String getKeyword() {
    return keyword;
  }

  public void setKeyword(String keyword) {
    this.keyword = keyword;
  }

  public String getStory1() {
    return story1;
  }

  public void setStory1(String story1) {
    this.story1 = story1;
  }

  public String getStory2() {
    return story2;
  }

  public void setStory2(String story2) {
    this.story2 = story2;
  }
}
